package com.BasicJava;

public class VariableExample 
{

	public static void main(String[] args) 
	{
		int a=10;
		a=20;
		int b=30;
		b=10;
		System.out.println("The value of a variable is :"+a);
		System.out.println("The value of b variable is :"+b);
		String x="LiveTech";
		System.out.println("The value of x variable is :"+x);
		double y=10.123;
		System.out.println("The value of y variable is :"+y);
		char p='a';
		System.out.println("The value of p variable is :"+p);
		float k=12;
		System.out.println("The value of k variable is :"+k);
	}

}
